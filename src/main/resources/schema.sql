CREATE TABLE IF NOT EXISTS users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR(255) NOT NULL,
    email VARCHAR(512) NOT NULL,
    password VARCHAR(255) NOT NULL,
    CONSTRAINT pk_user PRIMARY KEY (id),
    CONSTRAINT UQ_USER_EMAIL UNIQUE (email),
    CONSTRAINT UQ_USER_PASSWORD UNIQUE (password)
    );

CREATE TABLE IF NOT EXISTS category (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR(255) NOT NULL,
    CONSTRAINT pk_category PRIMARY KEY (id)
    );

CREATE TABLE IF NOT EXISTS offers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR(255) NOT NULL,
    model VARCHAR(255) NOT NULL,
    price DOUBLE,
    picture VARCHAR (255),
    url VARCHAR(255),
    CONSTRAINT pk_offer PRIMARY KEY (id)
    );

CREATE TABLE IF NOT EXISTS shops (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    name VARCHAR(255) NOT NULL,
    details VARCHAR(512),
    image VARCHAR(255),
    go_to_link VARCHAR(255),
    category_id BIGINT,
    offer_id BIGINT,
    CONSTRAINT pk_shop PRIMARY KEY (id),
    CONSTRAINT FK_SHOPS_ON_CATEGORY FOREIGN KEY (category_id) REFERENCES category (id),
    CONSTRAINT FK_SHOPS_ON_OFFERS FOREIGN KEY (offer_id) REFERENCES offers (id)
    );